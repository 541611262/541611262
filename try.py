# Variables and Data Types
temperature = 25 # integer
humidity = 70.5 # float
city = "New York" # string
is_raining = False # boolean a

# Printing variables and their types
print("Temperature:", temperature, "Type:", type(temperature))
print("Humidity:", humidity, "Type:", type(humidity))
print("City:", city, "Type:", type(city))
print("Is it raining?", is_raining, "Type:", type(is_raining))
